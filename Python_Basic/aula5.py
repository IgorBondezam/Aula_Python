# Operadores aritiméticos

"""
    + -- soma
    - -- subtração
    * -- multiplicação
    / -- divisão float
    // -- divisão int
    ** -- exponeciação
    % -- resto da divisão
    () -- altera a order das contas
"""

print((10*10))
print(10+10)
print(10-5)
print(10/2)
print(10//2)
print(1 // 5)
print(10%3)
print(10**10)
print(5 * "Igor\n")
print("Igor\n" * 5)
print(("Igor" + 5 * " " + "Bondezam" + 10 * " " + "França"))

print("\n")

print(1 / 4 )